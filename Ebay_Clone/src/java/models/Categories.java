package models;

/**
 *
 * @author ASUS
 */
public class Categories {

    private int id;
    private String name;

    // Getters and Setters
    public Categories() {
    }

    public Categories(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
